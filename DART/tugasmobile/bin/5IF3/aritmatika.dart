void main(List<String> args) {
  int x = 10;
  int y = 3;

  print("Operator Aritmatika");
  print("Hasil : ${x + y}"); 
  print("Hasil : ${x - y}"); 
  print("Hasil : ${x / y}"); 
  print("Hasil : ${x * y}"); 

  print("Operator Perbandingan");
  print("Hasil : ${x == y}");
  print("Hasil : ${x > y}");
  print("Hasil : ${x < y}");
  print("Hasil : ${x >= y}");
  print("Hasil : ${x <= y}");
}